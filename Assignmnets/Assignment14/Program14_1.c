#include<stdio.h>

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Function name : DisplayDigit
//  Description   : Used to accept number from user and display those digits in reverse
//  Input         : Integer
//  Output        : Integer
//  Author        : Saurabh Ravindra Bhonsle
//  Date          : 25 /10/2025
//
///////////////////////////////////////////////////////////////////////////////////////////

void DisplayDigit(int iNo)
{
  int iDigit = 0;

  if(iNo < 0)
 {
    iNo = -iNo;
 }

 while(iNo != 0)
 {
    iDigit =  iNo % 10;
    printf("%d\n",iDigit);
    iNo = iNo / 10;
 }
}
 

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function of the application
//
///////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
  
    printf("Enter number :");
    scanf("%d",&iValue);

    DisplayDigit(iValue);  
    return 0;
}

