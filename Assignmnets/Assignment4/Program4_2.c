#include<stdio.h>

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Function name : FactRev.
//  Description   : Accept number from user and display its of factors in decreasing order
//  Input         : Integer
//  Output        : Integer
//  Author        : Saurabh Ravindra Bhonsle
//  Date          : 21 /10/2025
//
///////////////////////////////////////////////////////////////////////////////////////////
void  FactRev(int iNo)
{
    int iCnt = 0;

    //updator
    if(iNo < 0)
    {
        iNo = -iNo;
    }
    for(iCnt = iNo / 2 ; iCnt >=1 ; iCnt--)
    {
        if(iNo % iCnt == 0)
        {
            printf("%d\t",iCnt);
        }
    }
  
    
}

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
///////////////////////////////////////////////////////////////////////////////////////////
int main()
{
    int iValue = 0;
   

    printf("Enter number ");
    scanf("%d",&iValue);

    FactRev(iValue);
   
    return 0;
}

