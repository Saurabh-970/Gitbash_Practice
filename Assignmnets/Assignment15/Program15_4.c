#include<stdio.h>

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Function name : MultiDigits
//  Description   : Used to accept number from user and return multiplication of those digi
//  Input         : Integer
//  Output        : Integer
//  Author        : Saurabh Ravindra Bhonsle
//  Date          : 25 /10/2025
//
///////////////////////////////////////////////////////////////////////////////////////////

int MultiDigits(int iNo)
{
   int iDigit = 0;
   int iMulti = 1;

  if(iNo < 0)
  {
   iNo = -iNo;
  }

  while(iNo != 0)
  {
   iDigit = iNo % 10;

   if(iDigit != 0)
   {
      iMulti = iMulti * iDigit;
   }
   iNo = iNo / 10;
  }
  return iMulti;


}
 

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function of the application
//
///////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    int bRet = 0;
  
    printf("Enter number :");
    scanf("%d",&iValue);

    bRet = MultiDigits(iValue);
    
    printf("%d",bRet);
  
    return 0;
}

