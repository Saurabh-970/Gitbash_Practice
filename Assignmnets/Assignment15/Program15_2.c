///////////////////////////////////////////////////////////////////////////////////////////
//
//  Function name : CountOdd
//  Description   : Used to accept number from user and return count of Odd numbers
//  Input         : Integer
//  Output        : Integer
//  Author        : Saurabh Ravindra Bhonsle
//  Date          : 25 /10/2025
//
///////////////////////////////////////////////////////////////////////////////////////////

int CountOdd(int iNo)
{
   int iDigit = 0;
   int iCount = 0;

   if(iNo < 0)
   {
      iNo = -iNo;
   }

   while(iNo != 0)
   {
      iDigit = iNo % 10;

      if(iDigit % 2 !=0)
      {
         iCount++;
      }
      iNo = iNo / 10;        
   }
   return iCount;


}
 

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function of the application
//
///////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    int bRet = 0;
  
    printf("Enter number :");
    scanf("%d",&iValue);

    bRet = CountOdd(iValue);
    
    printf("%d",bRet);
  
    return 0;
}

