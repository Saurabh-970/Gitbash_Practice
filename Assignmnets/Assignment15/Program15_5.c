///////////////////////////////////////////////////////////////////////////////////////////
//
//  Function name : EvenOddDiff
//  Description   : Used to accept number and display Even Odd Difference
//  Input         : Integer
//  Output        : Integer
//  Author        : Saurabh Ravindra Bhonsle
//  Date          : 25 /10/2025
//
///////////////////////////////////////////////////////////////////////////////////////////

int EvenOddDiff(int iNo)
{
   int iDigit = 0;
   int iEven = 0;
   int iOdd  = 0;
   int iDiff = 0;

  if(iNo < 0)
  {
   iNo = -iNo;
  }

  while(iNo != 0)
  {
   iDigit = iNo % 10;

   if(iDigit % 2 == 0)
   {
      iEven += iDigit; 
   }
   else
   {
      iOdd += iDigit;
   }
   iNo = iNo / 10;
  }
  iDiff = iEven - iOdd;
  return iDiff;


}
 

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function of the application
//
///////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    int bRet = 0;
  
    printf("Enter number :");
    scanf("%d",&iValue);

    bRet = EvenOddDiff(iValue);
    
    printf("%d",bRet);
  
    return 0;
}

