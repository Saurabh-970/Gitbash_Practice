void DisplayFactor(int iNo)
{
    int iCnt = 0;

    if(iNo <=0)
    {
        iNo = -iNo;
    }
    for(int iCnt =1; iCnt <= iNo /2; iCnt++)
    {
        if(iNo % iCnt==0)
        {
         printf("%d\t",iCnt);
        }
    }
    printf("\n");
}


//////////////////////////////////////////////////////////////////////////
//
//   Entry Point Function
// 
//////////////////////////////////////////////////////////////////////////
int main()
{
    int iValue = 0;
    
    printf("Enter number :");
    scanf("%d",&iValue);

    DisplayFactor(iValue);
    return 0;
}
