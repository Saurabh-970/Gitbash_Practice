//////////////////////////////////////////////////////////////////////////
//  
//  Function Name : DisplayFactor
//  Description   : Accept number from user that prints factors of that no
//  Input         : Integer
//  Output        : Integer
//  Author name   : Saurabh Ravindra Bhonsle.
//  Date          : 20/10/2025
//
