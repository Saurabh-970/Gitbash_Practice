#include<stdio.h>

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Function name : MultiDisplay
//  Description   : Used to print first 5 multiplies of N
//  Input         : Integer
//  Output        : Integer
//  Author        : Saurabh Ravindra Bhonsle
//  Date          : 25 /10/2025
//
///////////////////////////////////////////////////////////////////////////////////////////
void MultiDisplay(int iNo)
{
  int iCnt = 0;

  for(iCnt = 1; iCnt <= 5; iCnt++)
  {
    printf("%d\t",iNo * iCnt);
  }
   
}

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function of the application
//
///////////////////////////////////////////////////////////////////////////////////////////
int main()
{
    int iValue = 0;
    
    printf("Enter number :");
    scanf("%d",&iValue);

    MultiDisplay(iValue);
   
    return 0;
}

