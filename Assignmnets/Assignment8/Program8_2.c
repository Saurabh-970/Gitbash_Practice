#include<stdio.h>

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Function name : Number
//  Description   : Number is small(-50), medium(50-100) or large(100+)
//  Input         : Integer
//  Output        : Integer
//  Author        : Saurabh Ravindra Bhonsle
//  Date          : 25 /10/2025
//
///////////////////////////////////////////////////////////////////////////////////////////
void Number(int iNo)
{
  if(iNo <=50)
  {
    printf("Small:");
  }
  else if(iNo > 50 && iNo <= 100 )
  {
    printf("Medium");
  }
  else
  {
    printf("Large\n");
  }

   
}

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function of the application
//
///////////////////////////////////////////////////////////////////////////////////////////
int main()
{
    int iValue = 0;
    
    printf("Enter number :");
    scanf("%d",&iValue);

    Number(iValue);
   
    return 0;
}

