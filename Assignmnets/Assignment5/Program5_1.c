int CheckEvenOdd(int iNo)
{
   if((iNo % 2) == 0)
   {
        printf("%d is even even number ",iNo);
   }
   else
   {
        printf("%d is odd number",iNo);
   }
    
}

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
///////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int number;
    

    printf("Enter number ");
    scanf("%d",&number);

    CheckEvenOdd(number);
   
   
    return 0;
}

