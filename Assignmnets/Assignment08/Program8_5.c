#include<stdio.h>

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Function name : Table
//  Description   : Accept number and display its table in reverse
//  Input         : Integer
//  Output        : Integer
//  Author        : Saurabh Ravindra Bhonsle
//  Date          : 25 /10/2025
//
///////////////////////////////////////////////////////////////////////////////////////////
void Table(int iNo)
{
 if(iNo < 0)
 {
  iNo = -iNo;
 }

 int iCnt = 0;
 for(iCnt = 10 ; iCnt >= 1 ; iCnt--)
 {
    printf("%d ",iCnt * iNo);
 }

   
}

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function of the application
//
///////////////////////////////////////////////////////////////////////////////////////////
int main()
{
    int iValue = 0;
   
    
    printf("Enter number :");
    scanf("%d",&iValue);

    Table(iValue);
   
    return 0;
}

