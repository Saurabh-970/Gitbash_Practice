#include<stdio.h>

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Function name : FHtoCS
//  Description   : Used to accept temperature in Fahrenheit and convert it into celsius
//  Input         : Integer
//  Output        : Double
//  Author        : Saurabh Ravindra Bhonsle
//  Date          : 25 /10/2025
//
///////////////////////////////////////////////////////////////////////////////////////////

double FHtoCS(float fTemp)
{
  double dCelsius = 0.0;
  dCelsius = (fTemp -32) * (5.0/9.0);
  return dCelsius;
}

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function of the application
//
///////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    float fValue = 0.0;
    double  dRet = 0.0;

    printf("Enter temperature in Fahernheit :");
    scanf("%f",&fValue);

    dRet = FHtoCS(fValue);
    printf("Your Celsius is: %lf ",dRet);
    
    return 0;
}

