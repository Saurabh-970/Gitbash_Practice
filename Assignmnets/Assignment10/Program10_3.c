#include<stdio.h>

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Function name : KMtoMeter
//  Description   : Used to accept distance in Km and convert it into meter
//  Input         : Integer
//  Output        : Integer
//  Author        : Saurabh Ravindra Bhonsle
//  Date          : 25 /10/2025
//
///////////////////////////////////////////////////////////////////////////////////////////

int KMtoMeter(int iNo)
{
  int iSum = 1000;
  iSum = iSum * iNo;
  return iSum;
}

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function of the application
//
///////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    int iRet = 0;

    printf("Enter Distance :");
    scanf("%d",&iValue);

    iRet = KMtoMeter(iValue);
    printf("Your Meter is: %d ",iRet);
    
    return 0;
}

