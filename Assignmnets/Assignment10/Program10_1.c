#include<stdio.h>

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Function name : SquareMeter
//  Description   : Used to accept area in SqFt and return its Square Meter.
//  Input         : Integer
//  Output        : Double
//  Author        : Saurabh Ravindra Bhonsle
//  Date          : 25 /10/2025
//
///////////////////////////////////////////////////////////////////////////////////////////

double SquareMeter(int iValue)
{
  double dSqMt = 0.0;
  dSqMt = iValue * 0.0929;
  return dSqMt;
}

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function of the application
//
///////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    double dRet = 0.0;

    printf("Enter area in square feet :");
    scanf("%d",&iValue);

    dRet = SquareMeter(iValue);
    printf("Your Square is: %lf ",dRet);
    
    return 0;
}

