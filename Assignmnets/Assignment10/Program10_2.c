#include<stdio.h>

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Function name : RectArea
//  Description   : Used to accept width and height of rect and calculate its area
//  Input         : Float
//  Output        : Double
//  Author        : Saurabh Ravindra Bhonsle
//  Date          : 25 /10/2025
//
///////////////////////////////////////////////////////////////////////////////////////////
double RectArea(float fwidth, float fheight)
{
  double dArea = 0.0;
  dArea =   fwidth * fheight;
  return dArea;

}

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function of the application
//
///////////////////////////////////////////////////////////////////////////////////////////
int main()
{
    float fValue1 = 0.0;
    float fValue2 = 0.0;
    double dRet = 0.0;
   
    
    printf("Enter Width :");
    scanf("%f",&fValue1);

    printf("Enter Height :");
    scanf("%f",&fValue2);

    dRet = RectArea(fValue1,fValue2);
    printf("Your Area is: %lf ",dRet);
    
    return 0;
}

