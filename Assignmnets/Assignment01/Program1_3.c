void Display()
{
    int i = 0;
    i = 5;

    while(i >= 1)
    {
        printf("%d\n",i);
        i--;
    }
}

//////////////////////////////////////////////////////////////////////////
//
//   Entry Point Function
// 
//////////////////////////////////////////////////////////////////////////
int main()
{
    Display();
    return 0;
}