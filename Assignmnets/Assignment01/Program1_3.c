#include<stdio.h>
//////////////////////////////////////////////////////////////////////////
//  
//  Function Name : Display
//  Description   : Program to print 5 to 1 no on screen 
//  Input         : Integer
//  Output        : Integer
//  Author name   : Saurabh Ravindra Bhonsle.
//  Date          : 20/10/2025
