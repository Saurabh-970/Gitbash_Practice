#include<stdio.h>

//////////////////////////////////////////////////////////////////////////
//  
//  Function Name : Accept
//  Description   : Accept number and print * on screen
//  Input         : Integer
//  Output        : *
//  Author name   : Saurabh Ravindra Bhonsle.
//  Date          : 20/10/2025
//
////////////////////////////////////////////////////////////////////////// 

void Accept(int iNo)
{
    int iCnt = 0;
    for(iCnt =1; iCnt <= iNo; iCnt++)
    {
        printf("*\n");
    }
}


//////////////////////////////////////////////////////////////////////////
//
//   Entry Point Function
// 
//////////////////////////////////////////////////////////////////////////
int main()
{
    int iValue = 0;
    
    printf("Enter number :");
    scanf("%d",&iValue);

    Accept(iValue);
    return 0;
}