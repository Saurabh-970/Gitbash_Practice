int CheckLeapYear(int i)
{
   if((i % 4 == 0) && (i % 100 != 0) ||(i % 400 == 0))
   {
        printf("%d is a Leap Year ",i);
   }
   else
   {
        printf("%d is not a Leap year",i);
   }
    
}

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
///////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iYear;
    
    printf("Enter year :");
    scanf("%d",&iYear);
    CheckLeapYear(iYear);
        
    return 0;
}

