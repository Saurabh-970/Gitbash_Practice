int CheckNumberType(int iNum)
{
   if(iNum > 0)
   {
     printf(" %d :It is Positive number :",iNum);
   }
   else if(iNum < 0)
   {
     printf("%d :It is Negative Number :",iNum);
   }
   else
   {
     printf("it is zero");
   }
    
}

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
///////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iNumber;
    
    printf("Enter number :");
    scanf("%d",&iNumber);
    CheckNumberType(iNumber);
        
    return 0;
}

