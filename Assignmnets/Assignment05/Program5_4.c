///////////////////////////////////////////////////////////////////////////////////////////
//
//  Function name : CheckNumberType.
//  Description   : Used to check positive, negative, or zero
//  Input         : Integer
//  Output        : String
//  Author        : Saurabh Ravindra Bhonsle
