int FindMax(int iNo1, int iNo2)
{
   if(iNo1 > iNo2)
   {
       return iNo1;
   }
   else
   {
        return iNo2;
   }
    
}

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
///////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int num1 = 0;
    int num2 = 0;
    int result = 0;
    

    printf("Enter two number ");
    scanf("%d %d",&num1 , &num2);

    result = FindMax(num1, num2);
    printf("<Max number is %d\n",result);
        
    return 0;
}

