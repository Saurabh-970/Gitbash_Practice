void PrintEven(int iNo)
{
    
    if(iNo <=0)
    {
        return;
    }
    for(int iCnt =2; iCnt <= iNo; iCnt +=2)
    {
        printf("Your number is : ""%d\n",iCnt);
    }
    printf("\n");
}


//////////////////////////////////////////////////////////////////////////
//
//   Entry Point Function
// 
//////////////////////////////////////////////////////////////////////////
int main()
{
    int iValue = 0;
    
    printf("Enter number :");
    scanf("%d",&iValue);

    PrintEven(iValue);
    return 0;
}
