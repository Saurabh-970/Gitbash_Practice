///////////////////////////////////////////////////////////////////////////////////////////
//
//  Function name : EvenFact
//  Description   : Factorial of even number
//  Input         : Integer
//  Output        : Integer
//  Author        : Saurabh Ravindra Bhonsle
//  Date          : 25 /10/2025
//
///////////////////////////////////////////////////////////////////////////////////////////
int EvenFact(int iNo)
{
  if(iNo < 0)
  {
    iNo = -iNo;
  }
  int iCnt = 0;
  int Fact = 1;

  for(iCnt = 1; iCnt <= iNo; iCnt++)
  {
    if((iCnt % 2)==0)
    {
      Fact = Fact * iCnt;
    }
  }
  return Fact;

}

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function of the application
//
///////////////////////////////////////////////////////////////////////////////////////////
int main()
{
    int iValue = 0;
    int iRet = 0;
   
    
    printf("Enter number :");
    scanf("%d",&iValue);

    iRet = EvenFact(iValue);
    printf("Even Factorial is  : %d ",iRet);
    
    return 0;
}

