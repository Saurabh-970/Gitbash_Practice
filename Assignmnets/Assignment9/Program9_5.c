#include<stdio.h>

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Function name : FactorialDiff
//  Description   : Used to find Even AndOdd FactorialDiff of number
//  Input         : Integer
//  Output        : Integer
//  Author        : Saurabh Ravindra Bhonsle
//  Date          : 25 /10/2025
//
///////////////////////////////////////////////////////////////////////////////////////////
int FactorialDiff(int iNo)
{
  int iCnt = 0;
  int iEvenFact = 1;
  int iOddFact = 1;
  int iFactDiff = 0;

  if(iNo < 0)
  {
    iNo = -iNo;
  }
  for(iCnt = 1 ; iCnt <= iNo; iCnt++)
  {
    if((iCnt % 2)==0)
    {
      iEvenFact = iEvenFact * iCnt;
    }
    else
    {
      iOddFact = iOddFact * iCnt;
    }
  }
  iFactDiff =  iEvenFact - iOddFact;
  return iFactDiff;

}

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function of the application
//
///////////////////////////////////////////////////////////////////////////////////////////
int main()
{
    int iValue = 0;
    int iRet = 0;
   
    
    printf("Enter number :");
    scanf("%d",&iValue);

    iRet = FactorialDiff(iValue);
    printf("Factorial difference is  : %d ",iRet);
    
    return 0;
}

