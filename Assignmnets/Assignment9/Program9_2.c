///////////////////////////////////////////////////////////////////////////////////////////
//
//  Function name : DollarTOINR
//  Description   : Accept USD and return INR
//  Input         : Integer
//  Output        : Integer
//  Author        : Saurabh Ravindra Bhonsle
//  Date          : 25 /10/2025
//
