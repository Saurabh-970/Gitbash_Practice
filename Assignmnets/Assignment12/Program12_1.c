///////////////////////////////////////////////////////////////////////////////////////////
//
//  Function name : PrintFactors
//  Description   : Used to display all positive factors
//  Input         : Integer
//  Output        : Integer
//  Author        : Saurabh Ravindra Bhonsle
//  Date          : 25 /10/2025
//
///////////////////////////////////////////////////////////////////////////////////////////

void PrintFactors(int number)
{
  int iCnt = 0;

  if(number < 0)
  {
    number = - number;
  }
  
  for(iCnt =1; iCnt <= number ; iCnt++ )
  {
    if(number % iCnt == 0)
    {
        printf(" %d ",iCnt);
    }
  }
}

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function of the application
//
///////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int number ;

    printf("Enter number :");
    scanf("%d",&number);

    PrintFactors(number);
    
    return 0;
}

