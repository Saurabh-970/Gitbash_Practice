int SumofEvenFactors(int number)
{
  int iCnt = 0;
  int iCount = 0;

  if(number < 0)
  {
    number = - number;
  }
  
  for(iCnt =1; iCnt <= (number/2) ; iCnt++ )
  {
    if((number % iCnt )==0 && (iCnt %2)==0)
    {
      iCount = iCount + iCnt;
    }
  }
  return iCount;
}

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function of the application
//
///////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int number ;

    printf("Enter number :");
    scanf("%d",&number);

    printf("Sum of Even factor is :%d\n",SumofEvenFactors(number));
    
    
    return 0;
}

