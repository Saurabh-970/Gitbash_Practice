#include<stdio.h>

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Function name : CountFactors
//  Description   : Used to Count total factors of a number
//  Input         : Integer
//  Output        : Integer
//  Author        : Saurabh Ravindra Bhonsle
//  Date          : 25 /10/2025
//
///////////////////////////////////////////////////////////////////////////////////////////

int CountFactors(int number)
{
  int iCnt = 0;
  int iCount = 0;

  if(number < 0)
  {
    number = - number;
  }
  
  for(iCnt =1; iCnt <= number ; iCnt++ )
  {
    if(number % iCnt == 0)
    {
        iCount++;
    }
  }
  return iCount;
}

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function of the application
//
///////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int number ;

    printf("Enter number :");
    scanf("%d",&number);

    printf("Factor Count is :%d\n",CountFactors(number));
    
    
    return 0;
}

