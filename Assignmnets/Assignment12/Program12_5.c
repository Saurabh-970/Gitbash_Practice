#include<stdio.h>
#include<stdbool.h>

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Function name : DivisiblebyFive
//  Description   : Used to display no is divisible by 5 or not
//  Input         : Integer
//  Output        : String
//  Author        : Saurabh Ravindra Bhonsle
//  Date          : 25 /10/2025
//
///////////////////////////////////////////////////////////////////////////////////////////

bool DivisiblebyFive(int iValue)
{
  if((iValue % 5)==0)
  {
    return true;
  }
  else
  {
    return false;
  }
}

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function of the application
//
///////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int number ;

    printf("Enter number :");
    scanf("%d",&number);

    printf("%s\n\n",DivisiblebyFive(number) ? "Yes" : "No");
    
    
    return 0;
}

