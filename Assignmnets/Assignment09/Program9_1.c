///////////////////////////////////////////////////////////////////////////////////////////
//
//  Function name : Display
//  Description   : print * #  times of given N by user
//  Input         : Integer
//  Output        : Integer
//  Author        : Saurabh Ravindra Bhonsle
//  Date          : 25 /10/2025
//
///////////////////////////////////////////////////////////////////////////////////////////
void Display(int iNo)
{
 int iCnt =0;
 if(iNo < 0)
 {
    iNo = -iNo;
 }

 for(iCnt = 1; iCnt <= iNo; iCnt ++)
 {
    printf(" * ",iNo);
 }
 for(iCnt = 1; iCnt <= iNo; iCnt ++)
 {
    printf(" # ",iNo);
 }
    
   
}

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function of the application
//
///////////////////////////////////////////////////////////////////////////////////////////
int main()
{
    int iValue = 0;
   
    
    printf("Enter number :");
    scanf("%d",&iValue);

    Display(iValue);
   
    return 0;
}

