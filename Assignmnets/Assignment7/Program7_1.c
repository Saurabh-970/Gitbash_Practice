#include<stdio.h>

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Function name : Pattern
//  Description   : Accept number from user and print $ *
//  Input         : Integer
//  Output        : $ *
//  Author        : Saurabh Ravindra Bhonsle
//  Date          : 21 /10/2025
//
///////////////////////////////////////////////////////////////////////////////////////////
void Pattern(int iNo)
{
    int iCnt = 0;

  //updator
  if(iNo < 0)
  {
    iNo = -iNo;
  }  

  for(iCnt = 1; iCnt <= iNo; iCnt++)
  {
    printf(" $\t *\t ",iCnt);
  }
   
}

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
///////////////////////////////////////////////////////////////////////////////////////////
int main()
{
    int iValue = 0;
    
    printf("Enter number :");
    scanf("%d",&iValue);

    Pattern(iValue);
   
    return 0;
}

