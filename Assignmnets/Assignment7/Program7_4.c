#include<stdio.h>

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Function name : OddDisplay
//  Description   : Used to print Odd number till N number
//  Input         : Integer
//  Output        : Integer
//  Author        : Saurabh Ravindra Bhonsle
//  Date          : 25 /10/2025
//
///////////////////////////////////////////////////////////////////////////////////////////
void OddDisplay(int iNo)
{
  int iCnt = 0;

  if(iNo <= 0)
  {
      iNo = -iNo;
  }

  for(iCnt = 1; iCnt <= iNo; iCnt =  iCnt + 2)
  {
    if((iCnt % 2)!=0)
    {
      printf("%d \t",iCnt);
    }
  }
   
}

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function of the application
//
///////////////////////////////////////////////////////////////////////////////////////////
int main()
{
    int iValue = 0;
    
    printf("Enter number :");
    scanf("%d",&iValue);

    OddDisplay(iValue);
   
    return 0;
}

