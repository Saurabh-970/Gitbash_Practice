#include<stdio.h>

//////////////////////////////////////////////////////////////////////////
//  
//  Function Name : Display
//  Description   : Accept number from user <10 print Hello. >10 Demo
//  Input         : Integer
//  Output        : Hello or Demo
//  Author name   : Saurabh Ravindra Bhonsle.
//  Date          : 20/10/2025
//
