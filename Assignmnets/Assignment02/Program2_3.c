void Display(int iNo)
{
    
    // Updator
    if(iNo <=10)
    {
        printf("Hello:");
    }
    else
    {
        printf("Demo:");
    }
}


//////////////////////////////////////////////////////////////////////////
//
//   Entry Point Function
// 
//////////////////////////////////////////////////////////////////////////
int main()
{
    int iValue = 0;
    
    printf("Enter number :");
    scanf("%d",&iValue);

    Display(iValue);
    return 0;
}
