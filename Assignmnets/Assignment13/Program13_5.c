///////////////////////////////////////////////////////////////////////////////////////////
//
//  Function name : SumEvenNumber
//  Description   : Used to display sum of even number 
//  Input         : Integer
//  Output        : Integer
//  Author        : Saurabh Ravindra Bhonsle
//  Date          : 26 /10/2025
//
///////////////////////////////////////////////////////////////////////////////////////////

int SumEvenNumber(int iLimit)
{
  int iCnt = 0;
  int iSum = 0;

  for(iCnt = 1; iCnt <= iLimit; iCnt++)
  {
    iSum += iCnt * 2;
   
  }
  return iSum;
}

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function of the application
//
///////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int ilimt ;

    printf("Enter number :");
    scanf("%d",&ilimt);

    printf("%d\n",SumEvenNumber(ilimt));
    return 0;
}

