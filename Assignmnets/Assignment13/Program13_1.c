///////////////////////////////////////////////////////////////////////////////////////////
//
//  Function name : DivisiblebyFive
//  Description   : Used to display number from 1 to N
//  Input         : Integer
//  Output        : Integer
//  Author        : Saurabh Ravindra Bhonsle
//  Date          : 25 /10/2025
//
///////////////////////////////////////////////////////////////////////////////////////////

void PrintNumber(int iNo)
{
  int iCnt = 0;

  for(iCnt = 1; iCnt <= iNo; iCnt++)
  {
    printf("%d\t",iCnt);
  }
}

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function of the application
//
///////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int limt ;

    printf("Enter number :");
    scanf("%d",&limt);

    PrintNumber(limt);
    return 0;
}

