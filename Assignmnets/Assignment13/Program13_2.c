void PrintEvenNumber(int iLimit)
{
  int iCnt = 0;

  for(iCnt = 1; iCnt <= iLimit; iCnt++)
  {
    if((iCnt % 2)==0)
    {
       printf("%d\t",iCnt);
    }
   
  }
}

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function of the application
//
///////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int ilimt ;

    printf("Enter number :");
    scanf("%d",&ilimt);

    PrintEvenNumber(ilimt);
    return 0;
}

