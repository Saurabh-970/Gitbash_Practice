////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : percentage
//  Description   : Used to calculate percentage
//  Input         : Integer
//  Output        : Integer
//  Author        : Saurabh Ravindra Bhonsle
//  Date          : 21/10/2025
//
////////////////////////////////////////////////////////////////////////////////

float percentage(int iNo1, int iNo2)
{
    float fResult = 0.0f;

    if(iNo1 == 0)
    {
        return 0.0f;
    }
    fResult = ((float)iNo2 / (float)iNo1) * 100;

    return fResult;
}

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue1 = 0;
    int iValue2 = 0;
    float fRet = 0;

    printf("Plese enter total marks : ");
    scanf("%d", &iValue1);

    printf("Plese enter obtained marks : ");
    scanf("%d", &iValue2);

    fRet = percentage(iValue1, iValue2);
    printf("Percentage is : %.1f%%\n",fRet);

    return 0;
} // End main

