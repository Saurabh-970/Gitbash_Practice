#include<stdio.h>

//////////////////////////////////////////////////////////////////////////
//  
//  Function Name : Display
//  Description   : Accept number and print * on screen
//  Input         : Integer
//  Output        : *
//  Author name   : Saurabh Ravindra Bhonsle.
//  Date          : 20/10/2025
//
////////////////////////////////////////////////////////////////////////// 

void Display(int iNo)
{
    int iCnt = 0;

    // Updator
    if(iNo < 0)
    {
        iNo = -iNo;
    }
    while(iNo > iCnt)
    {
        printf("*\n");
        iNo--;

    }
}


//////////////////////////////////////////////////////////////////////////
//
//   Entry Point Function
// 
//////////////////////////////////////////////////////////////////////////
int main()
{
    int iValue = 0;
    
    printf("Enter number :");
    scanf("%d",&iValue);

    Display(iValue);
    return 0;
}
